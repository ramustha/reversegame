  private static List<String> generateSmall(String[] aStrings) {
    List<String> smalls = new ArrayList<>();
    for (String string : aStrings) {
      if (!smalls.contains(string) && string.length() < 5) {
        smalls.add(string);
      }
    }
    return smalls;
  }

